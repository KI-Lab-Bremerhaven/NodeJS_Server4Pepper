const router = require("express").Router();

/* * * * ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- * * * * 
 * * * ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- ----- * * * 
 * * * -----> R O U T E S <----- ----- ----- */

router.get("/docker-hbv-kms-http/logout", (req, res, next) => {
    ["connect.sid", "x-access-token"].forEach((key) => {
        res.clearCookie(key)
    });
    res.redirect("/docker-hbv-kms-http/");
});

